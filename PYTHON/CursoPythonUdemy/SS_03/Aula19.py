"""
Operadores de comparação (relacionais)
OP      Significado         Exemplo (True)
>       maior               2 > 1
>=      maior ou igual      2 >= 2
<       menor               1 < 2
<=      menor ou igual      2 <= 2
==      igual               'a' == 'a'
!=      diferente           'a' != 'b'
"""
maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'
print('Olha meu print aqui')
print(f'Maior: {maior}')
print(f'Maior ou igual: {maior_ou_igual}')
print(f'Menor: {menor}')
print(f'Menor ou Igual: {menor_ou_igual}')
print(f'Igual: {igual}')
print(f'Diferente: {diferente}')