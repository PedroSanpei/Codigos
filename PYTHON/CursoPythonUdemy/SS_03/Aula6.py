#  conversão de tipoes, coerção
#  type convertion, typecasting, coercion
#  é o ato de converter um tipo em outro
#  tipos imutáveis e primitivos:
#  str, int, float, bool

print(int('1') + 1) # -> Coercion
print('1' + str(1)) # -> Coercion
print(1 + float(1)) # -> Coercion
print(bool()) # -> Coercion
