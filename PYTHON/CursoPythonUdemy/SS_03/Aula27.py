"""
Fatiamento de strings
 012345678
 Olá mundo
-987654321
Fatiamento [i:f:p] [::]
Obs.: a função len retorna a qtd 
de caracteres da str
"""
variavel = 'Olá mundo'
print(variavel[8]) # Pegar o Indice específico
print(variavel[5:8]) # Pegar o Indice inicial até o indíce final
print(variavel[:9]) # Pegar até o indíce mencionado
print(variavel[::2]) # Pega o Indíce pulando de 2 em 2